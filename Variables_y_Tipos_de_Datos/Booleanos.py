x = 5
y = 3

print(x > y)  # True  (5 es mayor que 3)
print(x == y) # False (5 no es igual a 3)

# Operadores de comparacion

# ==	Igual a	5 == 5	True
# !=	Distinto de	5 != 3	True
# >	    Mayor que	7 > 4	True
# <	    Menor que	2 < 1	False
# >=	Mayor o igual que	5 >= 5	True
# <=	Menor o igual que	3 <= 2	False

# Operaciones logidos

# and	Y (True si ambas condiciones son True)	(5 > 3) and (2 < 4)	True
# or	O (True si al menos una condiciÃ³n es True)	(5 > 10) or (3 < 4)	True
# not	No (invierte el valor)	not True	False